=comment
Scalar:
	A scalar variable will precede by a dollar sing '$'
	
Array:
	An array variable will precede by an "at" sing '@'

Hash:
	A hash variable will precede by a % sing
	
	NOTE:
	Perl maintains every variable type in a separate namespace.
	So you can, without any fear of conflict, use the same name
	for a scalar variable, an array or a hash.
	This means the $foo and @foo and %foo are three different 
	variables.
	
=cut


